// Comments

// Java supports : single line comments (//) , and multi-line comments (/* ...*/)
//                 and documentation comments ( /** ... */ )

// single-line comments     : - all after "//" is line comment  ex.  // this is a comment

// multi-line comments
// (block comment)          : - all lines between "/*" and  "*/" are comments
// ex:
                                        /* this is a multi line
                                           comment
                                         */

// ex : single-line and multi-line comments
package Java_Tutorial;

public class T2_Comments1 {

    public static void main(String[] args) {

        // this is a single line comment
        System.out.println("Hello world!1");  // this is a single line comment

        /* this is
        a multi-line comment
         */

        System.out.println("Hello world!2");

    }
}
