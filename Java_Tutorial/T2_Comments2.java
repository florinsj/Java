// Comments

// Java supports : single line comments (//) , and multi-line comments (/* ...*/)
//                 and documentation comments ( /** ... */ )

// documentation comments   : - all lines between "/**" and  "*/" are documentation comments
//                            - are special comments, appeareance like multi-line comments
//                            - they generate external documentation of the code
//                            - Javadoc - a tool for generating java code documentation
// ex:
/**
 * this is a
 * documentation
 * comment
 */

 // ex. - documentation comments
package Java_Tutorial;

public class T2_Comments2 {
    public static void main(String[] args) {
        /** this is a documentation
         * comment
         */
        System.out.println("Hello world!");
        /** this is another documentation
         * comment
         */
    }
}
